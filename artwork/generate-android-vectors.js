#!/usr/bin/env node
'use strict';

// Generates the adaptive-icon foreground vector for the piece badge (the 'd'
// piece at grid rotation 5 in the party colorway — the same mark the tvOS
// icon and the legacy launcher webps carry, see artwork/tvos-icon.html).
// Pure geometry, no browser. Cells replicate CanvasUtils._stampHexPillow:
// rounded flat-top hex + white radial sheen + bottom shade line. No drop
// shadow — VectorDrawables can't blur. (The leanback banner is raster and
// comes from generate-android-icons.js, because its wordmark needs the real
// brand font.)
//
//   node artwork/generate-android-vectors.js

const path = require('path');
const fs = require('fs');

const ROOT = path.resolve(__dirname, '..');
const FG = path.resolve(ROOT, 'android/tv/src/main/res/drawable/ic_launcher_foreground.xml');

const SQRT3 = Math.sqrt(3);
// Party colorway, cell order left / center / bottom-right / top-nub —
// PARTY_PALETTE slots 5, 1, 0, 2 (keep in sync with badgeColors() in
// artwork/tvos-icon.html).
const COLORS = ['#F178D8', '#4ECDC4', '#FF6B6B', '#FFE066'];
const BADGE_CELLS = [[-1, 0], [0, 0], [1, 0], [1, -1]];

const f2 = (n) => n.toFixed(2).replace(/\.00$/, '.00');

// Badge cell centers, bounding-box centered on (cx, cy). s = cell grid size.
function cellCenters(cx, cy, s) {
  const R = s * 0.95;
  const pts = BADGE_CELLS.map(([q, r]) => {
    const col = q, row = r + ((q - (q & 1)) >> 1);
    return [1.5 * s * col, SQRT3 * s * (row + 0.5 * (col & 1))];
  });
  const minX = Math.min(...pts.map((p) => p[0])) - R;
  const maxX = Math.max(...pts.map((p) => p[0])) + R;
  const minY = Math.min(...pts.map((p) => p[1])) - SQRT3 / 2 * R;
  const maxY = Math.max(...pts.map((p) => p[1])) + SQRT3 / 2 * R;
  const mx = (minX + maxX) / 2, my = (minY + maxY) / 2;
  return pts.map(([x, y]) => [cx + x - mx, cy + y - my]);
}

// Rounded flat-top hex pathData (arcTo geometry: tangent length = A/tan(60°)),
// clockwise like the hand-authored vectors, sweep flag 1.
function roundedHexPath(cx, cy, R, A) {
  const T = A / Math.tan(Math.PI / 3);
  const v = [];
  for (let i = 0; i < 6; i++) {
    const a = Math.PI / 3 * i;
    v.push([cx + R * Math.cos(a), cy + R * Math.sin(a)]);
  }
  const toward = (from, to) => {
    const dx = to[0] - from[0], dy = to[1] - from[1];
    const len = Math.hypot(dx, dy);
    return [from[0] + dx / len * T, from[1] + dy / len * T];
  };
  const pIn = (i) => toward(v[i], v[(i + 5) % 6]);
  const pOut = (i) => toward(v[i], v[(i + 1) % 6]);
  let d = `M${f2(pOut(0)[0])},${f2(pOut(0)[1])}`;
  for (let i = 1; i <= 6; i++) {
    const j = i % 6;
    const [ix, iy] = pIn(j), [ox, oy] = pOut(j);
    d += `L${f2(ix)},${f2(iy)}A${f2(A)},${f2(A)} 0 0 1 ${f2(ox)},${f2(oy)}`;
  }
  return d + 'Z';
}

// One pillow cell: fill + sheen gradient + bottom shade line.
function cellXml(cx, cy, s, color, indent) {
  const R = s * 0.95;
  const A = R * 0.15;
  const pad = ' '.repeat(indent);
  const d = roundedHexPath(cx, cy, R, A);
  const lineY = cy + R * (SQRT3 / 2);
  const inset = A / SQRT3;
  const lw = Math.max(0.5, SQRT3 * R * 0.04);
  return `${pad}<path android:pathData="${d}" android:fillColor="${color}" />
${pad}<path android:pathData="${d}">
${pad}    <aapt:attr name="android:fillColor">
${pad}        <gradient android:type="radial"
${pad}            android:centerX="${f2(cx - R * 0.05)}" android:centerY="${f2(cy - R * 0.1)}"
${pad}            android:gradientRadius="${f2(R * 1.1)}">
${pad}            <item android:offset="0.0" android:color="#4DFFFFFF" />
${pad}            <item android:offset="1.0" android:color="#00FFFFFF" />
${pad}        </gradient>
${pad}    </aapt:attr>
${pad}</path>
${pad}<path android:pathData="M${f2(cx + R * 0.5 - inset)},${f2(lineY)}L${f2(cx - R * 0.5 + inset)},${f2(lineY)}" android:strokeColor="#40000000" android:strokeWidth="${f2(lw)}" android:strokeLineCap="round" />`;
}

function badgeXml(cx, cy, s, indent) {
  return cellCenters(cx, cy, s)
    .map(([x, y], i) => cellXml(x, y, s, COLORS[i], indent))
    .join('\n');
}

// ---- adaptive-icon foreground: badge at cell s=10.8dp, centered in the
// 108dp grid; the whole mark fits the 66dp mask-safe circle.
const fg = `<?xml version="1.0" encoding="utf-8"?>
<!-- HexStacker launcher icon foreground: the "piece badge" — the game's 'd'
     piece (grid rotation 5) in the party colorway, pillow cells mirroring the
     in-game render. Bounding-box centered in the 108dp grid, sized to fit the
     66dp mask-safe circle. GENERATED by artwork/generate-android-vectors.js —
     edit that script, not this file. -->
<vector xmlns:android="http://schemas.android.com/apk/res/android"
    xmlns:aapt="http://schemas.android.com/aapt"
    android:width="108dp"
    android:height="108dp"
    android:viewportWidth="108"
    android:viewportHeight="108">
${badgeXml(54, 54, 10.8, 4)}
</vector>
`;
fs.writeFileSync(FG, fg);
console.log('wrote  ', path.relative(ROOT, FG));
console.log('\nDone.');
